import fs from 'node:fs';
import path from 'node:path';

const inputPaths = process.argv.slice(2);

if (inputPaths.length === 0) {
  console.error('Usage: node scripts/i18n/generate-message-types.mjs <messages.json> [...messages.json]');
  process.exit(1);
}

let changed = false;

for (const inputPath of inputPaths) {
  const absolutePath = path.resolve(process.cwd(), inputPath);

  if (!fs.existsSync(absolutePath)) {
    console.error(`Messages file not found: ${absolutePath}`);
    process.exit(1);
  }

  if (!absolutePath.endsWith('.json')) {
    console.error(`Expected a .json messages file, received: ${absolutePath}`);
    process.exit(1);
  }

  const content = fs.readFileSync(absolutePath, 'utf8').trim();
  const declarationPath = absolutePath.replace(/\.json$/, '.d.json.ts');
  const declaration = `// This file is auto-generated by next-intl, do not edit directly.
// See: https://next-intl.dev/docs/workflows/typescript#messages-arguments

declare const messages: ${content};
export default messages;
`;

  const previous = fs.existsSync(declarationPath) ? fs.readFileSync(declarationPath, 'utf8') : null;

  if (previous !== declaration) {
    fs.writeFileSync(declarationPath, declaration);
    changed = true;
    console.log(`updated ${path.relative(process.cwd(), declarationPath)}`);
  } else {
    console.log(`unchanged ${path.relative(process.cwd(), declarationPath)}`);
  }
}

if (!changed) {
  process.exit(0);
}
